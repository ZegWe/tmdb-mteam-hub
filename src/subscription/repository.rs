#[derive(Debug, Clone, PartialEq, Eq)]
pub(crate) enum RepositoryError {
    InvalidInput {
        field: &'static str,
        message: String,
    },
    NotFound {
        key: SubscriptionKey,
    },
    RevisionConflict {
        key: SubscriptionKey,
        expected: Revision,
        actual: Revision,
    },
    /// A current optimistic detail update attempted to change scheduler gates
    /// while an execution attempt still owns the row.
    ///
    /// This is distinct from [`Self::RevisionConflict`]: the caller used the
    /// latest row revision, but the requested gate mutation is not owned by the
    /// generic detail port during running execution.
    ExecutionGateConflict {
        key: SubscriptionKey,
    },
    /// The attempted execution token is no longer the row's current owner.
    /// `current: None` means the attempt was consumed or independently
    /// superseded and the row is now idle.
    StaleAttempt {
        attempted: Box<ExecutionAttemptToken>,
        current: Option<Box<ExecutionAttemptToken>>,
    },
    /// The exact attempted token still exists, but its lease is no longer live
    /// according to the repository clock (`lease_until <= now`).
    LeaseExpired {
        attempt: ExecutionAttemptToken,
        lease_until: u64,
    },
    /// A lease-extension request would not move the absolute boundary forward.
    LeaseNotExtended {
        attempt: ExecutionAttemptToken,
        current_lease_until: u64,
        requested_lease_until: u64,
    },
    /// The attempted token is not the current open attempt. `current: None`
    /// includes an already-consumed token, so repeated/cross-terminal writes are
    /// stale rather than idempotent successes.
    StalePoll {
        account_key: String,
        attempted: PollAttemptToken,
        current: Option<PollAttemptToken>,
    },
    UnsupportedSchema {
        found: u32,
        maximum_supported: u32,
    },
    Unavailable {
        message: String,
    },
    CorruptData {
        message: String,
    },
    Internal {
        message: String,
    },
}

impl RepositoryError {
    fn invalid(field: &'static str, message: impl Into<String>) -> Self {
        Self::InvalidInput {
            field,
            message: message.into(),
        }
    }
}

impl fmt::Display for RepositoryError {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::InvalidInput { field, message } => {
                write!(formatter, "invalid {field}: {message}")
            }
            Self::NotFound { key } => write!(
                formatter,
                "subscription {}/{} was not found",
                key.account_key, key.subject_id
            ),
            Self::RevisionConflict {
                key,
                expected,
                actual,
            } => write!(
                formatter,
                "subscription {}/{} revision conflict: expected {}, actual {}",
                key.account_key,
                key.subject_id,
                expected.value(),
                actual.value()
            ),
            Self::ExecutionGateConflict { key } => write!(
                formatter,
                "subscription {}/{} scheduler gates cannot change during running execution",
                key.account_key, key.subject_id
            ),
            Self::StaleAttempt { attempted, current } => {
                write!(
                    formatter,
                    "execution attempt {} ({}) is stale for subscription {}/{}",
                    attempted.attempt_id().as_str(),
                    attempted.operation().as_str(),
                    attempted.key().account_key,
                    attempted.key().subject_id
                )?;
                if let Some(current) = current {
                    write!(
                        formatter,
                        "; current attempt is {} ({})",
                        current.attempt_id().as_str(),
                        current.operation().as_str()
                    )?;
                }
                Ok(())
            }
            Self::LeaseExpired {
                attempt,
                lease_until,
            } => write!(
                formatter,
                "execution attempt {} ({}) for subscription {}/{} expired at {}",
                attempt.attempt_id().as_str(),
                attempt.operation().as_str(),
                attempt.key().account_key,
                attempt.key().subject_id,
                lease_until
            ),
            Self::LeaseNotExtended {
                attempt,
                current_lease_until,
                requested_lease_until,
            } => write!(
                formatter,
                "execution attempt {} ({}) for subscription {}/{} keeps lease {} because requested boundary {} is not later",
                attempt.attempt_id().as_str(),
                attempt.operation().as_str(),
                attempt.key().account_key,
                attempt.key().subject_id,
                current_lease_until,
                requested_lease_until
            ),
            Self::StalePoll {
                account_key,
                attempted,
                current,
            } => {
                write!(
                    formatter,
                    "poll generation {} snapshot {} is stale for account {account_key}",
                    attempted.generation.value(),
                    attempted.snapshot_id.as_str()
                )?;
                if let Some(current) = current {
                    write!(
                        formatter,
                        "; current generation {} snapshot {}",
                        current.generation.value(),
                        current.snapshot_id.as_str()
                    )?;
                }
                Ok(())
            }
            Self::UnsupportedSchema {
                found,
                maximum_supported,
            } => write!(
                formatter,
                "subscription schema version {found} is unsupported; supported runtime version is {maximum_supported}"
            ),
            Self::Unavailable { message } => {
                write!(formatter, "subscription repository unavailable: {message}")
            }
            Self::CorruptData { message } => {
                write!(
                    formatter,
                    "subscription repository data is corrupt: {message}"
                )
            }
            Self::Internal { message } => {
                write!(formatter, "subscription repository failed: {message}")
            }
        }
    }
}

impl Error for RepositoryError {}

fn validated_text(field: &'static str, value: String) -> RepositoryResult<String> {
    if value.trim().is_empty() {
        return Err(RepositoryError::invalid(field, "value must not be blank"));
    }
    if value.contains('\0') {
        return Err(RepositoryError::invalid(
            field,
            "value must not contain a NUL byte",
        ));
    }
    Ok(value)
}

fn validate_text_bytes(field: &'static str, value: &str) -> RepositoryResult<()> {
    if value.contains('\0') {
        return Err(RepositoryError::invalid(
            field,
            "value must not contain a NUL byte",
        ));
    }
    Ok(())
}

fn validate_schedulability(
    schedulable: bool,
    blocked_reason: Option<&BlockedReason>,
) -> RepositoryResult<()> {
    match (schedulable, blocked_reason) {
        (true, None) | (false, Some(_)) => Ok(()),
        (true, Some(_)) => Err(RepositoryError::invalid(
            "blocked_reason",
            "a schedulable subscription cannot have a blocked reason",
        )),
        (false, None) => Err(RepositoryError::invalid(
            "blocked_reason",
            "an unschedulable subscription must have a blocked reason",
        )),
    }
}

fn validate_snapshot_schedulability(
    _media_kind: SubscriptionMediaKind,
    schedulable: bool,
    blocked_reason: Option<&BlockedReason>,
) -> RepositoryResult<()> {
    validate_schedulability(schedulable, blocked_reason)
}

fn validate_snapshot_records(
    snapshot_kind: &'static str,
    records: &[SnapshotRecord],
) -> RepositoryResult<()> {
    let mut subjects = BTreeSet::new();
    for record in records {
        record.validate()?;
        if !subjects.insert(record.subject_id.as_str()) {
            return Err(RepositoryError::invalid(
                "records",
                format!(
                    "{snapshot_kind} snapshot contains duplicate subject {}",
                    record.subject_id
                ),
            ));
        }
    }
    Ok(())
}

fn merge_source_observation(current: &mut WantedSourcePayload, observed: &WantedSourcePayload) {
    current.title = observed.title.trim().to_string();
    current.release_year = observed.release_year.or(current.release_year);
    replace_text_if_observed(&mut current.poster_url, &observed.poster_url);
    replace_text_if_observed(&mut current.cover_url, &observed.cover_url);
    replace_optional_text_if_observed(&mut current.original_title, &observed.original_title);
    replace_values_if_observed(&mut current.aka, &observed.aka);
    replace_values_if_observed(&mut current.languages, &observed.languages);
    replace_values_if_observed(&mut current.countries, &observed.countries);
    replace_values_if_observed(&mut current.genres, &observed.genres);
    replace_values_if_observed(&mut current.directors, &observed.directors);
    replace_values_if_observed(&mut current.actors, &observed.actors);
    replace_optional_text_if_observed(&mut current.date_published, &observed.date_published);
    replace_optional_text_if_observed(&mut current.duration, &observed.duration);
    replace_optional_text_if_observed(&mut current.summary, &observed.summary);
    current.rating_value = observed.rating_value.or(current.rating_value);
    current.rating_count = observed.rating_count.or(current.rating_count);
    replace_optional_text_if_observed(&mut current.category_text, &observed.category_text);
    current.tags = normalized_unique_values(&observed.tags);
    replace_optional_text_if_observed(&mut current.douban_date, &observed.douban_date);
    current.douban_sort_time = observed.douban_sort_time.or(current.douban_sort_time);
    current.douban_return_order = observed.douban_return_order.or(current.douban_return_order);
}

fn replace_text_if_observed(current: &mut String, observed: &str) {
    let observed = observed.trim();
    if !observed.is_empty() {
        *current = observed.to_string();
    }
}

fn replace_optional_text_if_observed(current: &mut Option<String>, observed: &Option<String>) {
    if let Some(observed) = observed
        .as_deref()
        .map(str::trim)
        .filter(|value| !value.is_empty())
    {
        *current = Some(observed.to_string());
    }
}

fn replace_values_if_observed(current: &mut Vec<String>, observed: &[String]) {
    let observed = observed
        .iter()
        .map(|value| value.trim())
        .filter(|value| !value.is_empty())
        .map(ToString::to_string)
        .collect::<Vec<_>>();
    if !observed.is_empty() {
        *current = observed;
    }
}

fn normalized_unique_values(values: &[String]) -> Vec<String> {
    let mut normalized = Vec::new();
    for value in values {
        let value = value.trim();
        if !value.is_empty() && !normalized.iter().any(|existing| existing == value) {
            normalized.push(value.to_string());
        }
    }
    normalized
}

fn validate_execution_result_operation(
    token: &ExecutionAttemptToken,
    disposition_operation: ExecutionOperation,
    payload_delta: &ExecutionPayloadDelta,
) -> RepositoryResult<()> {
    if token.operation() != disposition_operation {
        return Err(RepositoryError::invalid(
            "execution_disposition",
            format!(
                "{} attempt cannot persist a {} disposition",
                token.operation().as_str(),
                disposition_operation.as_str()
            ),
        ));
    }
    if token.operation() != payload_delta.operation() {
        return Err(RepositoryError::invalid(
            "payload_delta",
            format!(
                "{} attempt cannot persist a {} payload delta",
                token.operation().as_str(),
                payload_delta.operation().as_str()
            ),
        ));
    }
    payload_delta.validate_shape()
}

fn validate_unique_execution_updates<'a>(
    field: &'static str,
    values: impl IntoIterator<Item = &'a str>,
) -> RepositoryResult<()> {
    let mut unique = BTreeSet::new();
    for value in values {
        validated_text(field, value.to_string())?;
        if !unique.insert(value) {
            return Err(RepositoryError::invalid(
                field,
                "execution payload updates must use unique idempotency keys",
            ));
        }
    }
    Ok(())
}

fn merge_download_updates(
    current: &mut Vec<DownloadArtifactPayload>,
    updates: &[DownloadArtifactPayload],
) {
    for update in updates {
        if let Some(existing) = current
            .iter_mut()
            .find(|existing| existing.idempotency_key == update.idempotency_key)
        {
            existing.clone_from(update);
        } else {
            current.push(update.clone());
        }
    }
}

fn merge_link_updates(current: &mut Vec<LinkArtifactPayload>, updates: &[LinkArtifactPayload]) {
    for update in updates {
        if let Some(existing) = current
            .iter_mut()
            .find(|existing| existing.idempotency_key == update.idempotency_key)
        {
            existing.clone_from(update);
        } else {
            current.push(update.clone());
        }
    }
}

fn validate_attention_tags(tags: &[SubscriptionAttentionTag]) -> RepositoryResult<()> {
    let unique = tags.iter().copied().collect::<BTreeSet<_>>();
    if unique.len() != tags.len() {
        return Err(RepositoryError::invalid(
            "attention_tags",
            "attention tags must not contain duplicates",
        ));
    }
    Ok(())
}

fn validate_payload_media_kind(
    media_kind: SubscriptionMediaKind,
    payload: &SubscriptionPayload,
) -> RepositoryResult<()> {
    if media_kind == SubscriptionMediaKind::Movie && payload.tv.is_some() {
        return Err(RepositoryError::invalid(
            "payload.tv",
            "a movie subscription cannot carry TV detail",
        ));
    }
    Ok(())
}

fn validate_next_poll_at(
    terminal_field: &'static str,
    terminal_at: u64,
    next_poll_at: u64,
) -> RepositoryResult<()> {
    if next_poll_at < terminal_at {
        return Err(RepositoryError::invalid(
            "next_poll_at",
            format!("next poll time cannot precede {terminal_field}"),
        ));
    }
    Ok(())
}

