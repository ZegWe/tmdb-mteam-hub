/// The latest row plus the execution attempt atomically written by claim.
///
/// `detail.summary.head.revision` is the post-claim row revision for freshness
/// only. Future attempt terminals fence exclusively on `attempt.token`.
#[derive(Debug, Clone, PartialEq)]
pub(crate) struct ClaimedSubscription {
    detail: SubscriptionDetail,
    attempt: CurrentExecutionAttempt,
    replaced_expired_attempt: Option<ExpiredAttempt>,
}

impl ClaimedSubscription {
    pub(crate) fn try_new(
        detail: SubscriptionDetail,
        attempt: CurrentExecutionAttempt,
        replaced_expired_attempt: Option<ExpiredAttempt>,
    ) -> RepositoryResult<Self> {
        let head = &detail.summary().head;
        if &head.key != attempt.token().key() {
            return Err(RepositoryError::CorruptData {
                message: "claimed detail key does not match the current attempt token".to_string(),
            });
        }
        if head.execution_state != SubscriptionExecutionState::Running {
            return Err(RepositoryError::CorruptData {
                message: "a claimed subscription must expose running execution state".to_string(),
            });
        }
        let expected_operation = ExecutionOperation::for_lifecycle(head.lifecycle_state)
            .ok_or_else(|| RepositoryError::CorruptData {
                message: "a completed subscription cannot carry an execution attempt".to_string(),
            })?;
        if attempt.token().operation() != expected_operation {
            return Err(RepositoryError::CorruptData {
                message: format!(
                    "claimed operation {} does not match lifecycle {}",
                    attempt.token().operation().as_str(),
                    head.lifecycle_state.as_str()
                ),
            });
        }
        if let Some(expired) = &replaced_expired_attempt {
            if expired.token().key() != &head.key {
                return Err(RepositoryError::CorruptData {
                    message: "replaced expired attempt belongs to another subscription".to_string(),
                });
            }
            if expired.token().attempt_id() == attempt.token().attempt_id() {
                return Err(RepositoryError::CorruptData {
                    message: "an expired reclaim must allocate a new attempt ID".to_string(),
                });
            }
            if expired.token().operation() != attempt.token().operation() {
                return Err(RepositoryError::CorruptData {
                    message: "an expired reclaim must preserve the claimed operation".to_string(),
                });
            }
        }
        Ok(Self {
            detail,
            attempt,
            replaced_expired_attempt,
        })
    }

    pub(crate) const fn detail(&self) -> &SubscriptionDetail {
        &self.detail
    }

    pub(crate) const fn attempt(&self) -> &CurrentExecutionAttempt {
        &self.attempt
    }

    pub(crate) const fn replaced_expired_attempt(&self) -> Option<&ExpiredAttempt> {
        self.replaced_expired_attempt.as_ref()
    }
}

