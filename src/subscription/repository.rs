const MAX_EXECUTION_ATTEMPT_ID_BYTES: usize = 256;
const INITIAL_CLAIM_DUE_LIMIT: u32 = 1;

/// Scheduler operation persisted beside one execution attempt.
///
/// TV execution is deliberately absent while TV subscriptions are parked as
/// `tv_not_supported`. These labels are the only values a schema-v5 claim
/// adapter may persist in `claimed_operation`.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, PartialOrd, Ord)]
pub(crate) enum ExecutionOperation {
    Meta,
    Search,
    Progress,
    Link,
}

impl ExecutionOperation {
    pub(crate) const fn as_str(self) -> &'static str {
        match self {
            Self::Meta => "movie_meta",
            Self::Search => "movie_search",
            Self::Progress => "movie_progress",
            Self::Link => "movie_link",
        }
    }

    pub(crate) fn try_from_persisted(value: &str) -> RepositoryResult<Self> {
        match value {
            "movie_meta" => Ok(Self::Meta),
            "movie_search" => Ok(Self::Search),
            "movie_progress" => Ok(Self::Progress),
            "movie_link" => Ok(Self::Link),
            _ => Err(RepositoryError::CorruptData {
                message: format!("unknown claimed_operation {value:?}"),
            }),
        }
    }

    pub(crate) const fn for_lifecycle(lifecycle: SubscriptionLifecycleState) -> Option<Self> {
        match lifecycle {
            SubscriptionLifecycleState::Queued | SubscriptionLifecycleState::Meta => {
                Some(Self::Meta)
            }
            SubscriptionLifecycleState::Searching => Some(Self::Search),
            SubscriptionLifecycleState::Downloading => Some(Self::Progress),
            SubscriptionLifecycleState::Linking => Some(Self::Link),
            SubscriptionLifecycleState::Completed => None,
        }
    }
}

/// Collision-resistant nonce generated by the future schema-v5 claim adapter.
///
/// The domain contract stays transport-neutral: it validates persisted input
/// but does not prescribe UUIDs or a concrete random source.
#[derive(Debug, Clone, PartialEq, Eq, Hash, PartialOrd, Ord)]
pub(crate) struct ExecutionAttemptId(String);

impl ExecutionAttemptId {
    pub(crate) fn try_new(value: impl Into<String>) -> RepositoryResult<Self> {
        let value = validated_text("attempt_id", value.into())?;
        if value.len() > MAX_EXECUTION_ATTEMPT_ID_BYTES {
            return Err(RepositoryError::invalid(
                "attempt_id",
                format!("attempt ID must be at most {MAX_EXECUTION_ATTEMPT_ID_BYTES} UTF-8 bytes"),
            ));
        }
        Ok(Self(value))
    }

    pub(crate) fn as_str(&self) -> &str {
        &self.0
    }
}

