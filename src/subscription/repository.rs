/// Bounded exponential backoff used after an unsuccessful poll terminal.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) struct PollRetryPolicy {
    initial_delay_secs: u64,
    max_delay_secs: u64,
}

impl PollRetryPolicy {
    pub(crate) fn try_new(initial_delay_secs: u64, max_delay_secs: u64) -> RepositoryResult<Self> {
        if initial_delay_secs == 0 {
            return Err(RepositoryError::invalid(
                "initial_delay_secs",
                "initial retry delay must be greater than zero",
            ));
        }
        if max_delay_secs < initial_delay_secs {
            return Err(RepositoryError::invalid(
                "max_delay_secs",
                "maximum retry delay cannot be less than the initial delay",
            ));
        }
        Ok(Self {
            initial_delay_secs,
            max_delay_secs,
        })
    }

    pub(crate) const fn initial_delay_secs(self) -> u64 {
        self.initial_delay_secs
    }

    pub(crate) const fn max_delay_secs(self) -> u64 {
        self.max_delay_secs
    }

    /// Uses the initial delay for the first failure, then doubles until the cap.
    /// A zero count is treated conservatively as the initial delay as well; poll
    /// terminal operations normally pass a post-increment count of at least one.
    pub(crate) fn delay_secs(self, failure_count: u32) -> u64 {
        let mut delay = self.initial_delay_secs;
        for _ in 1..failure_count {
            delay = delay.saturating_mul(2).min(self.max_delay_secs);
            if delay == self.max_delay_secs {
                break;
            }
        }
        delay
    }

    pub(crate) fn next_poll_at(self, terminal_at: u64, failure_count: u32) -> u64 {
        terminal_at.saturating_add(self.delay_secs(failure_count))
    }
}

