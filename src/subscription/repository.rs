/// Operation-owned JSON delta returned by one execution attempt.
///
/// A terminal adapter applies this delta to the latest persisted payload after
/// it has established exact live-attempt ownership. Source/observation fields,
/// skip state, TV detail, unrelated artifacts, and all typed scheduler controls
/// are intentionally absent so a claim-time aggregate cannot overwrite newer
/// Poll or detail changes.
#[derive(Debug, Clone, PartialEq)]
pub(crate) enum ExecutionPayloadDelta {
    Meta,
    MovieMeta {
        source: Box<payload::WantedSourcePayload>,
    },
    TvMeta {
        tv: payload::TvDetailPayload,
        source: Option<Box<payload::WantedSourcePayload>>,
    },
    Search {
        candidates: Option<Vec<payload::CandidateMatchPayload>>,
        download_updates: Vec<DownloadArtifactPayload>,
    },
    Progress {
        download_updates: Vec<DownloadArtifactPayload>,
    },
    Link {
        download_updates: Vec<DownloadArtifactPayload>,
        link_updates: Vec<LinkArtifactPayload>,
    },
}

impl ExecutionPayloadDelta {
    pub(crate) const fn operation(&self) -> ExecutionOperation {
        match self {
            Self::Meta | Self::MovieMeta { .. } | Self::TvMeta { .. } => ExecutionOperation::Meta,
            Self::Search { .. } => ExecutionOperation::Search,
            Self::Progress { .. } => ExecutionOperation::Progress,
            Self::Link { .. } => ExecutionOperation::Link,
        }
    }

    fn validate_shape(&self) -> RepositoryResult<()> {
        match self {
            Self::Meta | Self::TvMeta { .. } => Ok(()),
            Self::MovieMeta { source } => source.validate(),
            Self::Search {
                download_updates, ..
            }
            | Self::Progress { download_updates } => validate_unique_execution_updates(
                "payload_delta.download_updates",
                download_updates
                    .iter()
                    .map(|artifact| artifact.idempotency_key.as_str()),
            ),
            Self::Link {
                download_updates,
                link_updates,
            } => {
                validate_unique_execution_updates(
                    "payload_delta.download_updates",
                    download_updates
                        .iter()
                        .map(|artifact| artifact.idempotency_key.as_str()),
                )?;
                validate_unique_execution_updates(
                    "payload_delta.link_updates",
                    link_updates
                        .iter()
                        .map(|artifact| artifact.idempotency_key.as_str()),
                )
            }
        }
    }

    /// Merge this operation-owned delta into the latest persisted payload.
    pub(crate) fn apply_to_latest(
        &self,
        key: &SubscriptionKey,
        latest: &mut SubscriptionPayload,
    ) -> RepositoryResult<()> {
        self.validate_shape()?;
        match self {
            Self::Meta => {}
            Self::MovieMeta { source } => {
                let mut merged = latest.source.clone();
                merge_source_observation(&mut merged, source.as_ref());
                latest.source = merged;
            }
            Self::TvMeta { tv, source } => {
                if let Some(source) = source {
                    let mut merged = latest.source.clone();
                    merge_source_observation(&mut merged, source.as_ref());
                    latest.source = merged;
                }
                latest.tv = Some(tv.clone());
            }
            Self::Search {
                candidates,
                download_updates,
            } => {
                if let Some(candidates) = candidates {
                    latest.candidates.clone_from(candidates);
                }
                merge_download_updates(&mut latest.artifacts.downloads, download_updates);
            }
            Self::Progress { download_updates } => {
                merge_download_updates(&mut latest.artifacts.downloads, download_updates);
            }
            Self::Link {
                download_updates,
                link_updates,
            } => {
                merge_download_updates(&mut latest.artifacts.downloads, download_updates);
                merge_link_updates(&mut latest.artifacts.links, link_updates);
            }
        }
        latest.validate_for(&key.account_key, &key.subject_id)
    }
}

/// Successful lifecycle disposition for one movie or TV execution operation.
/// Delayed variants carry relative durations, never caller-authored absolute
/// timestamps.
#[derive(Debug, Clone, PartialEq, Eq)]
pub(crate) enum FinishExecutionDisposition {
    MetaReady,
    SearchWaiting { retry_after: ExecutionScheduleDelay },
    SearchPushed,
    ProgressPending { retry_after: ExecutionScheduleDelay },
    ProgressDownloaded,
    LinkPendingDownload { retry_after: ExecutionScheduleDelay },
    LinkPlanned { retry_after: ExecutionScheduleDelay },
    LinkMoreRequired,
    LinkCompleted,
}

impl FinishExecutionDisposition {
    pub(crate) const fn operation(&self) -> ExecutionOperation {
        match self {
            Self::MetaReady => ExecutionOperation::Meta,
            Self::SearchWaiting { .. } | Self::SearchPushed => ExecutionOperation::Search,
            Self::ProgressPending { .. } | Self::ProgressDownloaded => ExecutionOperation::Progress,
            Self::LinkPendingDownload { .. }
            | Self::LinkPlanned { .. }
            | Self::LinkMoreRequired
            | Self::LinkCompleted => ExecutionOperation::Link,
        }
    }

    pub(crate) const fn next_lifecycle(&self) -> SubscriptionLifecycleState {
        match self {
            Self::MetaReady | Self::SearchWaiting { .. } | Self::LinkMoreRequired => {
                SubscriptionLifecycleState::Searching
            }
            Self::SearchPushed
            | Self::ProgressPending { .. }
            | Self::LinkPendingDownload { .. } => SubscriptionLifecycleState::Downloading,
            Self::ProgressDownloaded | Self::LinkPlanned { .. } => {
                SubscriptionLifecycleState::Linking
            }
            Self::LinkCompleted => SubscriptionLifecycleState::Completed,
        }
    }

    pub(crate) const fn next_delay(&self) -> Option<ExecutionScheduleDelay> {
        match self {
            Self::MetaReady
            | Self::SearchPushed
            | Self::ProgressDownloaded
            | Self::LinkMoreRequired => Some(ExecutionScheduleDelay(0)),
            Self::SearchWaiting { retry_after }
            | Self::ProgressPending { retry_after }
            | Self::LinkPendingDownload { retry_after }
            | Self::LinkPlanned { retry_after } => Some(*retry_after),
            Self::LinkCompleted => None,
        }
    }

    pub(crate) const fn waits_for_release(&self) -> bool {
        matches!(self, Self::SearchWaiting { .. })
    }

    pub(crate) const fn as_str(&self) -> &'static str {
        match self {
            Self::MetaReady => "meta_ready",
            Self::SearchWaiting { .. } => "search_waiting",
            Self::SearchPushed => "search_pushed",
            Self::ProgressPending { .. } => "progress_pending",
            Self::ProgressDownloaded => "progress_downloaded",
            Self::LinkPendingDownload { .. } => "link_pending_download",
            Self::LinkPlanned { .. } => "link_planned",
            Self::LinkMoreRequired => "link_more_required",
            Self::LinkCompleted => "link_completed",
        }
    }
}

