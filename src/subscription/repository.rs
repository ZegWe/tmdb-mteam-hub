/// Validated relative lease requested from the repository.
///
/// The adapter reads its injected clock and converts this TTL to an absolute
/// lease with checked arithmetic inside the claim transaction.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) struct ExecutionLeaseTtl(u64);

impl ExecutionLeaseTtl {
    pub(crate) fn try_new(seconds: u64) -> RepositoryResult<Self> {
        if seconds == 0 {
            return Err(RepositoryError::invalid(
                "lease_ttl_secs",
                "execution lease TTL must be greater than zero",
            ));
        }
        if seconds > i64::MAX as u64 {
            return Err(RepositoryError::invalid(
                "lease_ttl_secs",
                "execution lease TTL must fit the schema-v5 SQLite integer range",
            ));
        }
        Ok(Self(seconds))
    }

    pub(crate) const fn seconds(self) -> u64 {
        self.0
    }
}

/// Claims at most one due subscription in the first executable adapter slice.
///
/// Keeping `limit` explicit makes the bounded-read contract visible without
/// pretending that batch claim semantics have already been implemented.
#[derive(Debug, Clone, PartialEq, Eq)]
pub(crate) struct ClaimDueCommand {
    account_key: String,
    lease_ttl: ExecutionLeaseTtl,
    limit: u32,
}

