#[derive(Debug, Clone, PartialEq, Eq)]
pub(crate) struct SubscriptionProjection {
    pub(crate) title: String,
    pub(crate) release_year: Option<u16>,
    pub(crate) poster_url: String,
    pub(crate) category_text: Option<String>,
    pub(crate) douban_sort_time: Option<u64>,
}

impl SubscriptionProjection {
    pub(crate) fn from_source(source: &WantedSourcePayload) -> RepositoryResult<Self> {
        source.validate()?;
        if source
            .release_year
            .is_some_and(|year| !(1..=9999).contains(&year))
        {
            return Err(RepositoryError::invalid(
                "source.release_year",
                "release year must be between 1 and 9999",
            ));
        }
        validate_text_bytes("source.poster_url", &source.poster_url)?;
        if let Some(category_text) = &source.category_text {
            validate_text_bytes("source.category_text", category_text)?;
        }
        Ok(Self {
            title: source.title.clone(),
            release_year: source.release_year,
            poster_url: source.poster_url.clone(),
            category_text: source.category_text.clone(),
            douban_sort_time: source.douban_sort_time,
        })
    }

    pub(crate) fn validate_against(&self, payload: &SubscriptionPayload) -> RepositoryResult<()> {
        let expected = Self::from_source(&payload.source)?;
        if self != &expected {
            return Err(RepositoryError::CorruptData {
                message: "subscription projection does not match record_json payload".to_string(),
            });
        }
        Ok(())
    }
}

/// Applies one upstream observation to an existing JSON-owned payload.
///
/// Snapshot source data is enrichment input, not a replacement aggregate. This
/// helper changes only `source` and `observation.last_seen_at`; issues, skip
/// state, candidates, TV detail, and artifacts remain owned by their respective
/// workflows. Missing optional enrichment keeps the last useful value, matching
/// the schema-v4 refresh behavior.
impl SubscriptionPayload {
    pub(crate) fn merge_snapshot_observation(
        &mut self,
        observed_source: &WantedSourcePayload,
        observed_at: u64,
    ) -> RepositoryResult<()> {
        self.validate()?;
        SubscriptionProjection::from_source(observed_source)?;
        if observed_at < self.observation.last_seen_at {
            return Err(RepositoryError::invalid(
                "observed_at",
                "snapshot observation cannot precede the existing last observation",
            ));
        }

        let mut merged_source = self.source.clone();
        merge_source_observation(&mut merged_source, observed_source);
        SubscriptionProjection::from_source(&merged_source)?;
        self.source = merged_source;
        self.observation.last_seen_at = observed_at;
        self.validate()
    }
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub(crate) struct SubscriptionHead {
    pub(crate) key: SubscriptionKey,
    pub(crate) revision: Revision,
    pub(crate) active: bool,
    pub(crate) inactive_at: Option<u64>,
    pub(crate) last_seen_snapshot_id: Option<SnapshotId>,
    pub(crate) media_kind: SubscriptionMediaKind,
    pub(crate) schedulable: bool,
    pub(crate) blocked_reason: Option<BlockedReason>,
    pub(crate) lifecycle_state: SubscriptionLifecycleState,
    pub(crate) execution_state: SubscriptionExecutionState,
    pub(crate) next_attempt_at: Option<u64>,
    pub(crate) retry_count: u32,
    pub(crate) max_retries: u32,
    pub(crate) retry_blocked: bool,
    pub(crate) force_eligible_once: bool,
    pub(crate) updated_at: u64,
}

impl SubscriptionHead {
    pub(crate) fn validate(&self) -> RepositoryResult<()> {
        if self.active == self.inactive_at.is_some() {
            return Err(RepositoryError::CorruptData {
                message: "active and inactive_at columns disagree".to_string(),
            });
        }
        if self.schedulable == self.blocked_reason.is_some() {
            return Err(RepositoryError::CorruptData {
                message: "schedulable and blocked_reason columns disagree".to_string(),
            });
        }
        if !self.active
            && (self.execution_state != SubscriptionExecutionState::Idle
                || self.next_attempt_at.is_some())
        {
            return Err(RepositoryError::CorruptData {
                message: "an inactive subscription must be idle with no next attempt".to_string(),
            });
        }
        if !self.schedulable && self.next_attempt_at.is_some() {
            return Err(RepositoryError::CorruptData {
                message: "an unschedulable subscription cannot have a next attempt".to_string(),
            });
        }
        if self.lifecycle_state == SubscriptionLifecycleState::Completed
            && self.next_attempt_at.is_some()
        {
            return Err(RepositoryError::CorruptData {
                message: "a completed subscription cannot have a next attempt".to_string(),
            });
        }
        if self.execution_state == SubscriptionExecutionState::Running
            && (!self.active
                || !self.schedulable
                || self.lifecycle_state == SubscriptionLifecycleState::Completed)
        {
            return Err(RepositoryError::CorruptData {
                message: "a running subscription must be active, schedulable and non-completed"
                    .to_string(),
            });
        }
        let retry_blocked = self.max_retries > 0 && self.retry_count >= self.max_retries;
        if self.retry_blocked != retry_blocked {
            return Err(RepositoryError::CorruptData {
                message: "retry_blocked does not match retry_count/max_retries".to_string(),
            });
        }
        if self.force_eligible_once
            && (!self.active
                || !self.schedulable
                || self.lifecycle_state == SubscriptionLifecycleState::Completed
                || self.next_attempt_at.is_none())
        {
            return Err(RepositoryError::CorruptData {
                message: "force_eligible_once requires an active schedulable due record"
                    .to_string(),
            });
        }
        Ok(())
    }
}

