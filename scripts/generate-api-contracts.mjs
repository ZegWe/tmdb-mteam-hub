import { createHash } from "node:crypto";
import { spawnSync } from "node:child_process";
import { mkdtempSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { resolve } from "node:path";

const root = resolve(import.meta.dirname, "..");
const schemaPath = resolve(root, "src/http/openapi.json");
const outputPath = resolve(root, "frontend/src/shared/api/contracts.js");
const check = process.argv.includes("--check");
const document = JSON.parse(readFileSync(schemaPath, "utf8"));
const schemas = document.components?.schemas;
const schemaDigest = createHash("sha256").update(JSON.stringify(document)).digest("hex");

if (document.openapi !== "3.1.0" || !schemas) {
  throw new Error("src/http/openapi.json must be an OpenAPI 3.1 document with component schemas");
}

function refName(ref) {
  const prefix = "#/components/schemas/";
  if (!ref?.startsWith(prefix)) throw new Error(`unsupported schema ref: ${ref}`);
  return ref.slice(prefix.length);
}

function typeExpression(schema = {}) {
  if (schema.$ref) return refName(schema.$ref);
  if (schema.enum) return schema.enum.map((value) => JSON.stringify(value)).join(" | ");
  if (Array.isArray(schema.type)) {
    return schema.type
      .map((type) => typeExpression({ ...schema, type }))
      .filter((value, index, values) => values.indexOf(value) === index)
      .join(" | ");
  }
  switch (schema.type) {
    case "string":
      return "string";
    case "integer":
    case "number":
      return "number";
    case "boolean":
      return "boolean";
    case "null":
      return "null";
    case "array":
      return `${typeExpression(schema.items)}[]`;
    case "object":
      return schema.additionalProperties === true ? "Record<string, unknown>" : "object";
    default:
      return "unknown";
  }
}

function typedef(name, schema) {
  if (schema.enum) return `/** @typedef {${typeExpression(schema)}} ${name} */`;
  if (schema.type === "object" && schema.properties) {
    const required = new Set(schema.required || []);
    return [
      "/**",
      ` * @typedef {object} ${name}`,
      ...Object.entries(schema.properties).map(
        ([property, propertySchema]) =>
          ` * @property {${typeExpression(propertySchema)}${required.has(property) ? "" : "="}} ${property}`,
      ),
      " */",
    ].join("\n");
  }
  return `/** @typedef {${typeExpression(schema)}} ${name} */`;
}

const enumConstants = {
  SubscriptionMediaKind: "SUBSCRIPTION_MEDIA_KINDS",
  SubscriptionLifecycleState: "SUBSCRIPTION_LIFECYCLE_STATES",
  SubscriptionExecutionState: "SUBSCRIPTION_EXECUTION_STATES",
  SubscriptionAttentionTag: "SUBSCRIPTION_ATTENTION_TAGS",
};

const unformattedGenerated = [
  "/**",
  " * Generated from src/http/openapi.json by scripts/generate-api-contracts.mjs.",
  " * Do not edit this file directly; run `npm run generate:api-contracts`.",
  " */",
  `// schema-sha256: ${schemaDigest}`,
  "",
  ...Object.entries(schemas).flatMap(([name, schema]) => [typedef(name, schema), ""]),
  "/**",
  " * @typedef {object} NormalizedSubscriptionSummaryPage",
  " * @property {string | null} next_cursor",
  " * @property {string[]} ordered_ids",
  " * @property {Record<string, SubscriptionSummaryDto>} records",
  " */",
  "/** @typedef {NormalizedSubscriptionSummaryPage & {next_cursor: null}} NormalizedSubscriptionSummaryState */",
  "",
  ...Object.entries(enumConstants).flatMap(([schemaName, constant]) => [
    `/** @type {readonly ${schemaName}[]} */`,
    `export const ${constant} = Object.freeze(${JSON.stringify(schemas[schemaName].enum)});`,
  ]),
  "export const SUBSCRIPTION_SUMMARY_FIELDS = Object.freeze(",
  `  ${JSON.stringify(schemas.SubscriptionSummaryDto.required)},`,
  ");",
  "",
].join("\n");

function formatGenerated(source) {
  const temporaryRoot = mkdtempSync(resolve(tmpdir(), "tmdb-mteam-api-contracts-"));
  const temporaryFile = resolve(temporaryRoot, "contracts.js");
  try {
    writeFileSync(temporaryFile, source);
    const formatter = spawnSync(
      resolve(root, "node_modules/.bin/vp"),
      ["check", "--fix", "--no-lint", temporaryFile],
      { cwd: root, encoding: "utf8" },
    );
    if (formatter.status !== 0) {
      throw new Error(
        formatter.stderr || formatter.stdout || "failed to format generated API contracts",
      );
    }
    return readFileSync(temporaryFile, "utf8");
  } finally {
    rmSync(temporaryRoot, { recursive: true, force: true });
  }
}

const generated = formatGenerated(unformattedGenerated);

const current = readFileSync(outputPath, "utf8");

if (check) {
  if (current !== generated) {
    throw new Error("frontend API contracts are stale; run npm run generate:api-contracts");
  }
} else if (current !== generated) {
  writeFileSync(outputPath, generated);
}
